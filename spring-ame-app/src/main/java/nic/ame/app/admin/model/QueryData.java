package nic.ame.app.admin.model;

public class QueryData {

	
	private String forceNo;
	private String unitNo;
	private String boardId;
	private int unitType;
	private String iralNo;
	
	public String getForceNo() {
		return forceNo;
	}
	public void setForceNo(String forceNo) {
		this.forceNo = forceNo;
	}
	public String getUnitNo() {
		return unitNo;
	}
	public void setUnitNo(String unitNo) {
		this.unitNo = unitNo;
	}
	public String getBoardId() {
		return boardId;
	}
	public void setBoardId(String boardId) {
		this.boardId = boardId;
	}
	public int getUnitType() {
		return unitType;
	}
	public void setUnitType(int unitType) {
		this.unitType = unitType;
	}
	public String getIralNo() {
		return iralNo;
	}
	public void setIralNo(String iralNo) {
		this.iralNo = iralNo;
	}
	
}
